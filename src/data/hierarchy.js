// Helper functions to get administrative data
export const getProvinceById = (id) => {
  return rwandaProvinces.find(province => province.id === id);
};

export const getDistrictById = (provinceId, districtId) => {
  const province = getProvinceById(provinceId);
  return province?.districts.find(district => district.id === districtId);
};

export const getSectorById = (provinceId, districtId, sectorId) => {
  const district = getDistrictById(provinceId, districtId);
  return district?.sectors.find(sector => sector.id === sectorId);
};

export const getAllProvinces = () => rwandaProvinces;

export const getDistrictsByProvince = (provinceId) => {
  const province = getProvinceById(provinceId);
  return province?.districts || [];
};

export const getSectorsByDistrict = (provinceId, districtId) => {
  const district = getDistrictById(provinceId, districtId);
  return district?.sectors || [];
};
